char binToInt( int l_binarna)
{
    int l_decymalna = 0;
    for(int i=0;i<8;i++)
    {
        int potega = pow(10,i);
        if(potega%9 == 0)
        {
            potega += 1;
        }

        if((l_binarna/potega)%2 == 1)
        {
            int potega2 = pow(2,i);
            l_decymalna += potega2;
        }
    }

    return l_decymalna;
}


